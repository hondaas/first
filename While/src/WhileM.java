
public class WhileM {

	void gugu(int number) {

		int i = 0;

		while (i < 9) {

			i++;

			System.out.println(number + "x" + i + "=" + (number * i));
		}
	}

	void guif(int number) {
		int i = 0;
		while (i < 90) {

			i++;

			System.out.println(number + "x" + i + "=" + (number * i));

		}
	}

}
